struct sensor_bios_data_packed {
	u8 version;
	u8 sku;
	u8 guid_csi2[16];
	u8 devfunction;
	u8 bus;
	u32 dphylinkenfuses;
	u32 clockdiv;
	u8 link;
	u8 lanes;
	u32 csiparams[10];
	u32 maxlanespeed;
	u8 sensorcalibfileidx;
	u8 sensorcalibfileidxInMBZ[3];
	u8 romtype;
	u8 vcmtype;
	u8 platforminfo;
	u8 platformsubinfo;
	u8 flash;
	u8 privacyled;
	u8 degree;
	u8 mipilinkdefined;
	u32 mclkspeed;
	u8 controllogicid;
	u8 reserved1[3];
	u8 mclkport;
	u8 reserved2[13];
} __attribute__((__packed__));

void dump_bios_data(struct sensor_bios_data_packed *data) {
    printf("clockdiv: %d\n", data->clockdiv);
    printf("link: %d\n", data->link);
    printf("lanes: %d\n", data->lanes);
    printf("mclkspeed: %d\n", data->mclkspeed);
    printf("mclkport: %d\n", data->mclkport);
	printf("\n");
}

int main() {
    struct sensor_bios_data_packed *camf_data =
		(struct sensor_bios_data_packed *)camf_ssdb;
    struct sensor_bios_data_packed *camr_data =
		(struct sensor_bios_data_packed *)camr_ssdb;

    printf("CAMF:");
	dump_bios_data(camf_data);

    printf("CAMR:");
	dump_bios_data(camr_data);

    return 0;
}

